package operators_practice;

public class Arthamatic {

	public static void main(String[] args) {
		    
			    
			    int a = 4;
			    int queen;

			    // assign value using =
			    queen = a;
			    System.out.println("queen using =: " + queen);

			    // assign value using =+
			    queen += a;
			    System.out.println("queen using +=: " + queen);

			    // assign value using =*
			    queen *= a;
			    System.out.println("queen using *=: " + queen);
			  }
		}
			

	


