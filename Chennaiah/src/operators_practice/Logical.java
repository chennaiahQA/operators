package operators_practice;

public class Logical {

	public static void main(String[] args) {
		   int n=800;
	       int m=600;
	       System.out.println(n>m && n>m);
	       System.out.println(n>m || n>m);
	       System.out.println(n>=m || n<=m);
	       System.out.println(n!=m &&  n==m);
		}
}

	


