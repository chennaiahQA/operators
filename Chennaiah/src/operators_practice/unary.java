package operators_practice;

public class unary {

	public static void main(String[] args) {
		int x=40;
        int y=++x;
        System.out.println(y);
       
     // post increment
        int g=96;
        System.out.println(g++);
        
        //pre decrement
        int a=680;
        int b=--a;
        System.out.println(b);
        
        //post decrement
        int H=69;
        System.out.println(H--);

	}

}
